create table public.usuarios (
    id_usuario bigint generated by default as identity primary key,
    username text unique not null,
    password_hash text not null,
    nombre text not null,
    rol text not null check (rol in ('administrador', 'vendedor')),
    activo boolean not null default true,
    fecha_creacion timestamptz not null default now()
);

create table public.clientes (
    id_cliente bigint generated by default as identity primary key,
    identificacion text unique not null,
    nombre text not null,
    apellido text not null,
    email text unique not null,
    telefono text,
    fecha_registro timestamptz not null default now()
);

create table public.ventas (
    id_venta bigint generated by default as identity primary key,
    id_cliente bigint not null
        references public.clientes(id_cliente) on delete restrict,
    id_usuario bigint not null
        references public.usuarios(id_usuario) on delete restrict,
    fecha_venta timestamptz not null default now(),
    subtotal numeric(12,2) not null default 0 check (subtotal >= 0),
    impuestos numeric(12,2) not null default 0 check (impuestos >= 0),
    total numeric(12,2) not null default 0 check (total >= 0)
);

create table public.detalle_ventas (
    id_detalle bigint generated by default as identity primary key,
    id_venta bigint not null
        references public.ventas(id_venta) on delete restrict,
    id_producto bigint not null
        references public.productos(id_producto) on delete restrict,
    cantidad integer not null check (cantidad > 0),
    precio_unitario numeric(10,2) not null check (precio_unitario > 0),
    descuento numeric(10,2) not null default 0 check (descuento >= 0),
    check (descuento <= cantidad * precio_unitario)
);

create table public.historial_inventario (
    id_movimiento bigint generated by default as identity primary key,
    id_producto bigint not null
        references public.productos(id_producto) on delete restrict,
    id_usuario bigint not null
        references public.usuarios(id_usuario) on delete restrict,
    tipo_movimiento text not null
        check (tipo_movimiento in ('entrada', 'salida')),
    cantidad integer not null check (cantidad > 0),
    motivo text not null,
    fecha_movimiento timestamptz not null default now()
);

do $$
begin
